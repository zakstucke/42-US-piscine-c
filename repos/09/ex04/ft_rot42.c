char	*ft_rot42(char *str)
{
	int		x;

	x = 0;
	while (str[x] != '\0')
	{
		if (str[x] >= 'A' && str[x] <= 'Z')
		{
			str[x] = str[x] + 16;
			if (str[x] > 'Z')
				str[x] = 'A' + (str[x] - 'Z' - 1);
		}
		if (str[x] >= 'a' && str[x] <= 'z')
		{
			str[x] = str[x] + 16;
			if (str[x] > 'z')
				str[x] = 'a' + (str[x] - 'z' - 1);
		}
		x++;
	}
	return (str);
}
