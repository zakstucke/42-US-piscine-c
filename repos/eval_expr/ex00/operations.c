int	ft_sub(int first, int second)
{
	return (first - second);
}

int	ft_add(int first, int second)
{
	return (first + second);
}

int	ft_mul(int first, int second)
{
	return (first * second);
}

int	ft_div(int first, int second)
{
	return (first / second);
}

int	ft_mod(int first, int second)
{
	return (first % second);
}
