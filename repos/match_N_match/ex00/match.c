int		match(char *s1, char *s2)
{
	while (*s1 != '\0')
	{
		if (*s2 == '*')
		{
			if (match(s1 + 1, s2))
				return (1);
			else if (match(s1, s2 + 1))
				return (1);
			else
				return (0);
		}
		else if (*s1 == *s2)
		{
			s1++;
			s2++;
		}
		else
			return (0);
	}
	while (*s2 == '*')
		s2++;
	return (*s1 == *s2);
}
